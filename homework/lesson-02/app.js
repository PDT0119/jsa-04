function remainder(a, b) {
  let c = a % b;
  return c;
}
console.log(remainder(4, 9));
function perimeterRectangle(a, b) {
  let c = (a + b) * 2;
  return c;
}
console.log(perimeterRectangle(10, 20));
function points(a, b) {
  let aa = a * 2;
  let bb = b * 3;
  let c = aa + bb;
  return c;
}
console.log(points(1, 3));
function animalsLeg(a, b, c) {
  let aa = a * 2;
  let bb = (b + c) * 4;
  let cc = aa + bb;
  return cc;
}
console.log(animalsLeg(1, 2, 3));
function footballPoints(a, b, c) {
  let aa = a * 3;
  let cc = aa + b;
  return cc;
}
console.log(footballPoints(5, 0, 2));
function sumPolygon(a) {
  let aa = (a - 2) * 180;
  return aa;
}
console.log(sumPolygon(4));
function calculateFuel(a) {
  let b = a * 100;
  var c = a < 11 ? 100 : b;
  return c;
}
console.log(calculateFuel(3));
function yearsInOneHouse(a, b) {
  let aa = a / (b + 1);
  return aa;
}
console.log(yearsInOneHouse(15, 2));
function frames(a, b) {
  let aa = a * b * 60;
  return aa;
}
console.log(frames(10, 1));
